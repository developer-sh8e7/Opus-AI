// ─── Anti-repetition: track last N responses to avoid repeats ───
const _recentResponses: string[] = [];
const MAX_RECENT = 6;

function pickResponse(responses: string[]): string {
  // Filter out recently used responses when possible
  const available = responses.filter((r) => !_recentResponses.includes(r));
  const pool = available.length > 0 ? available : responses;
  const chosen = pool[Math.floor(Math.random() * pool.length)];

  // Track it
  _recentResponses.push(chosen);
  if (_recentResponses.length > MAX_RECENT) _recentResponses.shift();

  return chosen;
}


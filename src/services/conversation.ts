function normalize(text: string): string {
  return text
    .normalize('NFKC')
    .toLowerCase()
    .replace(/[\u064B-\u065F\u0670\u0640]/g, '')
    .replace(/[أإآ]/g, 'ا')
    .replace(/ى/g, 'ي')
    .replace(/ة/g, 'ه')
    .replace(/[^\p{L}\p{N}\s]/gu, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * الكلمات المفتاحية التي تشير إلى طلب فعل (action) وليس مجرد محادثة
 */
const ACTION_TERMS = [
  'احذف', 'حذف', 'سو', 'سوي', 'انشئ', 'أنشئ', 'اضف', 'أضف', 'غير', 'غيّر',
  'عدل', 'عدّل', 'حظر', 'بان', 'طرد', 'كيك', 'كتم', 'تايم', 'روم', 'قناة',
  'رتبة', 'رول', 'صلاحية', 'برمشن', 'سيرفر', 'متجر', 'ايمبد', 'embed',
  'delete', 'create', 'change', 'ban', 'kick', 'timeout', 'role', 'channel',
  'server', 'رومات', 'رتب', 'رولات', 'صلاحيات', 'برمشنات', 'منشن',
  'دسكونكت', 'دسكنوكت', 'ديسكونكت', 'ديسكنكت', 'voicekick', 'فويس',
  'صوتي', 'سكرين', 'شير', 'دسكنكته', 'دسكنكته', 'بث',
];

function hasActionIntent(text: string): boolean {
  return ACTION_TERMS.some((term) => text.includes(term));
}

/**
 * التعرف على ما إذا كان النص مجرد محادثة اجتماعية قصيرة.
 * إذا كان كذلك → رد برد مناسب عشوائي (غير متكرر).
 * إذا كان يحوي طلب فعل أو طويل → رجوع null ليمرره للذكاء الاصطناعي.
 */
export function getConversationReply(input: string): string | null {
  const text = normalize(input);

  // إذا كانت الرسالة طويلة (> 100 حرف) → ليست محادثة اجتماعية
  if (text.length > 100) return null;

  // إذا احتوت على كلمات فعل → ليست محادثة
  if (hasActionIntent(text)) return null;

  // البحث عن نمط مطابق
  for (const entry of greetingResponses) {
    if (entry.pattern.test(text)) {
      const index = Math.floor(Math.random() * entry.responses.length);
      return entry.responses[index];
    }
  }

  // كل ما عدا ذلك يمر للذكاء الاصطناعي
  return null;
}

  'اصنع', 'أصنع', 'ابني', 'ابن', 'نظم', 'صمم', 'شغل', 'وقف',
  'ثبت', 'ارسل', 'أرسل', 'حط', 'اعط', 'أعط', 'ارشفه', 'أرشفه',
